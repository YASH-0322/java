
        //TYPE CASTING

public class typecasting {
    public static void main (String [] args ){
        System.out.println(9/2);
        //by using type casting
       System.out.println((float)9/2);
        System.out.println(Math.round((float)9/2));
        System.out.println(Math.floor((float)9/2));
        System.out.println(Math.ceil((float)9/2));
        System.out.println(Math.pow((float)9/2,2));



        }
}
