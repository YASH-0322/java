     /*WHILE LOOP
     SYNTAX
     START;
     WHILE(CONDITION){
     STATEMENT

     CHANGING CONDITION ;
     }

*/
public class Whileloop {
    public static void main (String []args){
        int a =4;
        int i=1;//start condition
        while(i<=a){//CONDITION
            System.out.println("hello");//statement
            i++;//changing condition
        }

    }
}
